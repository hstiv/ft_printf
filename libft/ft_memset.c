#include <string.h>

void	*ft_memset(void *b, int c, size_t len)
{
	size_t			a;

	a = 0;
	if (len > 0)
	{
		while (len > a)
		{
			(((unsigned char*)b)[a]) = c;
			a++;
		}
	}
	return (&b[0]);
}
