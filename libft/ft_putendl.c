#include <unistd.h>

void	ft_putendl(char const *s)
{
	int		a;

	a = 0;
	if (!s)
		return ;
	while (s[a] != '\0')
	{
		write(1, &s[a], sizeof(s[a]));
		a++;
	}
	write(1, "\n", sizeof(char));
}
