int	ft_strcmpi(char const *s1, char const *s2)
{
	int		a;

	a = -1;
	if ((!s1 || !s2) || ((!s1[a] && s2[a]) || (!s2[a] && s1[a])))
		return (0);
	while (s1[++a] != '\0' || s2[a] != '\0')
	{
		if (s1[a] >= 'a' && s1[a] <= 'z')
		{
			if ((s1[a] != s2[a]) && (s1[a] != (s2[a] - ('a' - 'A'))))
				return (0);
		}
		else if (s1[a] >= 'A' && s1[a] <= 'Z')
		{
			if ((s1[a] != s2[a]) && (s1[a] != (s2[a] + ('a' - 'A'))))
				return (0);
		}
		else
		{
			if ((s1[a] < 'a' || s1[a] > 'z') && (s1[a] < 'A' || s1[a] > 'Z')
					&& ((unsigned char*)s1)[a] != ((unsigned char*)s2)[a])
				return (0);
		}
	}
	return (1);
}
