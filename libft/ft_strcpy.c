#include <string.h>

char	*ft_strcpy(char *dst, const char *src)
{
	size_t	a;

	a = 0;
	while (src[a] != '\0')
	{
		dst[a] = src[a];
		a++;
	}
	dst[a] = '\0';
	return (&dst[0]);
}
